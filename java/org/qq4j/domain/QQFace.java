package org.qq4j.domain;

public class QQFace {

    private String faceCode = null;

    private String faceString = null;

    public String getFaceCode() {
        return this.faceCode;
    }

    public void setFaceCode(final String faceCode) {
        this.faceCode = faceCode;
    }

    public String getFaceString() {
        return this.faceString;
    }

    public void setFaceString(final String faceString) {
        this.faceString = faceString;
    }
}
